uint8_t c1A = 0;
uint8_t c1B = 0;
uint8_t c2A = 0;
uint8_t c2B = 0;

void draw(uint8_t* screen)
{
  static uint8_t xbuffer[COLS];
  static uint8_t ybuffer[ROWS];
  uint8_t c2a = c2A;
  uint8_t c2b = c2B;
  uint8_t c1a = c1A;
  uint8_t c1b = c1B;
  for (int i = 0; i < ROWS; ++i) {
    ybuffer[i] = sinustable[c1a] + sinustable[c1b];
    c1a += 4;
    c1b += 9;
  }
  c1A += 3;
  c1B -= 5;
  c2a = c2A;
  c2b = c2B;
  for (int i = 0; i < COLS; ++i) {
    xbuffer[i] = sinustable[c2a] + sinustable[c2b];
    c2a += 3;
    c2b += 7;
  }
  c2A += 2;
  c2B -= 3;
  for (int i = 0; i < ROWS; ++i) {
#pragma unroll
    for (int j = 0; j < COLS; ++j) {
      POKE(screen++, xbuffer[j] + ybuffer[i]);
    }
  }
}

void generate_charset()
{
  static const uint8_t bits[8] = { 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80 };

  for (int cnt = 0; cnt < SINESIZE; ++cnt) {
    const uint8_t sine = sinustable[cnt];
    for (int i = 0; i < 8; ++i) {
      uint8_t char_pattern = 0;
      for (int j = 0; j < 8; ++j) {
        if ((rand8(0xff) & 0xff) > sine) {
          char_pattern |= bits[j];
        }
      }
      POKE(CHARSET + cnt * 8 + i, char_pattern);
    }
    if ((cnt & 0x07) == 0) {
      putchar('.');
    }
  }
}

/** Set MEGA65 speed to 3.5 Mhz */
void speed_mode3()
{
  uint8_t val = PEEK(VICIV_CONTROLB) | 0b01000000; // set FAST bit
  POKE(VICIV_CONTROLB, val);
  val = PEEK(VICIV_CONTROLC) & 0b10111111; // unset VFAST
  POKE(VICIV_CONTROLC, val);
}

int main()
{
  generate_charset();
  speed_mode3();
  while (1) {
    /* Build page 1, then make it visible */
    draw((uint8_t*)SCREEN1);
    POKE(VICII_MEMORY_CONTROL, PAGE1);

    /* Build page 2, then make it visible */
    draw((uint8_t*)SCREEN2);
    POKE(VICII_MEMORY_CONTROL, PAGE2);
  }
}
